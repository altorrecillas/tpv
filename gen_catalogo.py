# Parse
productos = []
cat_actual = None
refs = set()
for raw in DATA.strip().splitlines():
    line = raw.strip()
    if not line:
        continue
    if line.startswith("[") and line.endswith("]"):
        cat_actual = line[1:-1]
        assert cat_actual in EMOJI, cat_actual
        continue
    m = re.match(r"^(\d{4})\s+(.+)\|([\d.]+)$", line)
    assert m, "No parsea: " + line
    ref, nombre, precio = m.group(1), m.group(2).strip(), float(m.group(3))
    assert ref not in refs, "Ref duplicada: " + ref
    refs.add(ref)
    productos.append({
        "id": "p" + ref, "ref": ref, "nombre": nombre, "barcode": ref,
        "compra": precio, "precio": precio, "iva": 10, "cat": cat_actual,
        "emoji": EMOJI[cat_actual],
    })

categorias = [{"id": c[0], "nombre": c[1], "emoji": c[2]} for c in CATS]

# Comprobación de nombres duplicados dentro de la misma categoría
seen = {}
for p in productos:
    key = (p["cat"], p["nombre"].lower())
    if key in seen:
        print("AVISO nombre duplicado en categoría:", p["cat"], p["nombre"])
    seen[key] = True

print("Categorías:", len(categorias))
print("Productos:", len(productos))
por_cat = {}
for p in productos:
    por_cat[p["cat"]] = por_cat.get(p["cat"], 0) + 1
for c in categorias:
    print(f"  {c['nombre']:<20} {por_cat.get(c['id'],0)}")

# --- Generar bloque JS para datosIniciales ---
def js_array(items):
    out = io.StringIO()
    out.write("[\n")
    for it in items:
        out.write("    " + json.dumps(it, ensure_ascii=False) + ",\n")
    out.write("  ]")
    return out.getvalue()

js_block = "  const cats = " + js_array(categorias) + ";\n"
js_block += "  const productos = " + js_array(productos) + ";\n"

# --- Reescribir index.html ---
with open("index.html", encoding="utf-8") as f:
    html = f.read()

ini = html.index("function datosIniciales(){")
cuerpo_ini = html.index("\n", ini) + 1            # tras la llave de apertura
ret = html.index("\n  return {", cuerpo_ini)      # antes del return
nuevo = html[:cuerpo_ini] + js_block + html[ret+1:]
with open("index.html", "w", encoding="utf-8") as f:
    f.write(nuevo)
print("index.html actualizado.")

